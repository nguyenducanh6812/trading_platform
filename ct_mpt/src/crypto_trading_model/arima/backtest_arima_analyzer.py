    def save_analysis_results(self, analysis_results: Dict, output_dir: str = "arima_analysis_results"):
        """Save comprehensive ARIMA analysis results to files."""
        try:
            output_path = Path(output_dir)
            output_path.mkdir(exist_ok=True)
            
            risk_profile = analysis_results.get('risk_profile', 'unknown')
            
            # Save detailed ARIMA analysis
            with open(output_path / f"arima_analysis_{risk_profile}.txt", "w") as f:
                f.write(f"ARIMA Analysis Results - {risk_profile.title()} Profile\n")
                f.write("=" * 60 + "\n\n")
                
                # Portfolio statistics
                f.write("Portfolio Statistics:\n")
                f.write("-" * 20 + "\n")
                stats = analysis_results.get('portfolio_statistics', {})
                for key, value in stats.items():
                    if isinstance(value, float):
                        f.write(f"{key}: {value:.6f}\n")
                    else:
                        f.write(f"{key}: {value}\n")
                
                f.write("\n")
                
                # ARIMA results
                if 'arima_analysis' in analysis_results:
                    arima_summary = analysis_results['arima_analysis']['analysis_summary']
                    f.write("ARIMA Analysis Summary:\n")
                    f.write("-" * 25 + "\n")
                    f.write(f"Optimal Order: ARIMA{arima_summary['optimal_order']}\n")
                    f.write(f"Best AIC: {arima_summary['best_aic']:.4f}\n")
                    f.write(f"Significant Coefficients: {arima_summary['num_significant_coefficients']}\n")
                    f.write(f"Forecast Ready: {arima_summary['forecast_ready']}\n")
                    f.write(f"Stationarity: {arima_summary['is_stationary']}\n")
                    f.write(f"Differencing Applied: {arima_summary['differencing_applied']}\n")
            
            self.logger.info(f"ARIMA analysis results saved to: {output_path}")
            
        except Exception as e:
            self.logger.error(f"Failed to save analysis results: {e}")